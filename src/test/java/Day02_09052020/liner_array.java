package Day02_09052020;

public class liner_array {
    public static void main(String[] args) {

        //linear string array example
        String[] username = new String[]{"jon", "Kenneth", "milad", "milad",};
        System.out.println("second user name is " + username[1]);


//linear integer array example
        int[] numbers = new int[]{1, 100, 300, 500};

        System.out.println("first int value is " + numbers[0]);
        //i want to get sum of first 2 data
        System.out.println("total of first two int data is " + (numbers[0] +
                numbers[1]));


    }//end of java class


}//end of java class
